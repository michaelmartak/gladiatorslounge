package org.oaktownrpg.jgladiator.framework.ccg;

/**
 * Availability of FOIL in a set or card print.
 * 
 * @author michaelmartak
 *
 */
public enum FoilAvailability {

    /**
     * Card is available in foil or non-foil versions
     */
    FOIL_OR_NONFOIL,
    /**
     * Card is only available in non-foil
     */
    NONFOIL_ONLY,
    /**
     * Card is only available in foil
     */
    FOIL_ONLY,

}
